// Set the target date (YYYY-MM-DD format)
const targetDate = new Date('2024-04-16T08:59:59');

function updateCountdown() {
  const now = new Date().getTime(); // Get the current time in milliseconds
  const difference = targetDate.getTime() - now; // Calculate the difference in milliseconds

  if (difference <= 0) {
    // If the countdown is over, display a message
    document.getElementById('countown').innerHTML = 'Countdown over!';
  } else {
    // Calculate days, hours, minutes, and seconds
    const days = Math.floor(difference / (1000 * 60 * 60 * 24));
    const hours = Math.floor((difference % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((difference % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((difference % (1000 * 60)) / 1000);
    
    // Display the countdown
    document.getElementById('countown').innerHTML = `${days}d ${hours}h ${minutes}m ${seconds}s`;
  }
}

// Update the countdown every second
setInterval(updateCountdown, 1000);

// Initial call to start the countdown
updateCountdown();

